#include <stdio.h>
#include <stdlib.h>

int main(void)
{ 
    int a, b = 5, c;
    double x, y = -.5, z;
    
    printf("a=");
    scanf("%d", &a);
    
    // Анализ: x = c = a
    c = a;      // c получает целое значение a
    x = c;      // x получает значение c, преобразованное в double
    printf("x = c = a : a=%d c=%d x=%.2f\n", a, c, x);
    
    // Анализ: a += b
    a = a + b;  // a увеличивается на значение b
    printf("a += b : a=%d\n", a);
    
    // Анализ: x *= b+a
    x = x * (b + a); // x умножается на сумму b и a
    printf("x *= b+a : x=%.2lf\n", x);
    
    // Анализ: b += a--
    b = b + a;  // сначала используется текущее значение a
    a = a - 1;  // затем a уменьшается на 1
    printf("b += a-- : a=%d b=%d\n", a, b);
    
    // Анализ: x -= ++c
    c = c + 1;  // сначала c увеличивается на 1
    x = x - c;  // затем x уменьшается на новое значение c
    printf("x -= ++c : c=%d x=%.2lf\n", c, x);
    
    // Анализ: c = a/b (целочисленное деление)
    c = a / b;
    printf("c = a/b : c=%d\n", c);
    
    // Анализ: c = a%b (остаток от деления)
    c = a % b;
    printf("c = a%%b : c=%d\n", c);
    
    // Анализ: y += (a+1)/a++
    int temp1 = a + 1;    // числитель
    int temp2 = a;        // знаменатель (текущее значение a)
    a = a + 1;            // затем a увеличивается
    y = y + (double)temp1 / temp2; // целочисленное деление, затем преобразование
    printf("y += (a+1)/a++ : a=%d y=%.3f\ty=%.1f\n", a, y, y);
    
    // Анализ: b = 3*(y=-.6)+2*b+1
    y = -0.6;            // сначала y присваивается -0.6
    b = 3 * y + 2 * b + 1; // затем вычисляется выражение
    printf("b = 3*(y=-.6)+2*b+1 : b=%d y=%.1f\n", b, y);
    
    // Анализ: z = a/2 (целочисленное деление)
    z = a / 2;           // целочисленное деление, затем преобразование в double
    printf("z = a/2 : z=%.2lf\n", z);
    
    // Анализ: z = (double)a/2 (вещественное деление)
    z = (double)a / 2;   // явное преобразование к double
    printf("z = (double)a/2 : z=%.2lf\n", z);
    
    // Анализ: y = (x = 5.7)/2
    x = 5.7;             // x присваивается 5.7
    y = x / 2;           // y получает половину x
    printf("y = (x = 5.7)/2 : x=%.2lf y=%.2lf\n", x, y);
    
    // Анализ: y = (int)x/2
    y = (int)x / 2;      // x преобразуется в int (5), затем целочисленное деление
    printf("y = (int)x/2 : y=%.2f\n", y);
    
    // Анализ: сложное выражение (разбито на части)
    double part1 = (b - 3) / 2;          // целочисленное деление
    double part2 = x / 5;
    double part3 = (c / -2);             // целочисленное деление  
    double part4 = 1 / 4 * z;            // 1/4 = 0 (целочисленное), затем 0*z
    double part5 = y;                    // текущее значение y
    y = y + 1;                           // y++ - увеличиваем после использования
    b = b + 1;                           // ++b - увеличиваем до использования
    double part6 = b / 3.0;              // вещественное деление
    
    z = part1 - part2 + part3 + part4 - part5 + part6;
    
    printf("z = (b-3)/2 - x/5 +(c/-2) + 1/4*z - y++ + ++b/3. :\n");
    printf("a=%d b=%d c=%d x=%.2lf y=%.2lf z=%.2lf\n", a, b, c, x, y, z);
    
    system("pause");
    return 0;
}